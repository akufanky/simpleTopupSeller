    private void cbPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbPriceActionPerformed
        String price = cbPrice.getSelectedItem().toString();
        
        switch (price) {
            case "Rp5.000" -> {
                cbPrice.removeItemAt(0);
                lbTotalPrice.setText("Rp7.000");
            }
            case "Rp10.000" -> {
                cbPrice.removeItemAt(0);
                lbTotalPrice.setText("Rp12.000");
            }
            case "Rp20.000" -> {
                cbPrice.removeItemAt(0);
                lbTotalPrice.setText("Rp22.000");
            }
            case "Rp50.000" -> {
                cbPrice.removeItemAt(0);
                lbTotalPrice.setText("Rp52.000");
            }
            case "Rp100.000" -> {
                cbPrice.removeItemAt(0);
                lbTotalPrice.setText("Rp102.000");
            } default -> {
                lbTotalPrice.setText("-");
            }
        }
    }//GEN-LAST:event_cbPriceActionPerformed

